// Medidas del triángulo
let A = parseInt (prompt ("digita el primer valor")) 
let B = parseInt (prompt ("digita el Segundo  valor")) 

// Calcular la hipotenusa
let hipotenusa = Math.sqrt((A**2) + (B**2));

// Mostrar el resultado en la página web
document.writeln("La hipotenusa del triángulo es: " + hipotenusa + " cm");
